public class ExemploForArray {
    public static void main(String[] args) {
        String alunos [] = {"JÚLIA","MARCOS", "FELIPE", "JORGE", "ANA", "LETICÍA"};

        for(int x=0; x<alunos.length; x ++){
            System.out.println("O aluno no indíce " + x + " é " + alunos[x]);
        };
    }
}
